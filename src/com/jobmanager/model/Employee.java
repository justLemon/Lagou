package com.jobmanager.model;

public class Employee {
    private String  userName;
	private String 	name;
	private String 	sex;
	private String 	highestEducation;
	private String 	workYear;
	private String 	status;
	private String 	email;
	private String 	phone;
	private String 	type;
	private String 	city;
	private String 	positionType;
	private String 	positionName;
	private String 	salarys;
	private String 	myRemark;
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getHighestEducation() {
		return highestEducation;
	}
	public void setHighestEducation(String highestEducation) {
		this.highestEducation = highestEducation;
	}
	public String getWorkYear() {
		return workYear;
	}
	public void setWorkYear(String workYear) {
		this.workYear = workYear;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getPositionType() {
		return positionType;
	}
	public void setPositionType(String positionType) {
		this.positionType = positionType;
	}
	public String getPositionName() {
		return positionName;
	}
	public void setPositionName(String positionName) {
		this.positionName = positionName;
	}
	public String getSalarys() {
		return salarys;
	}
	public void setSalarys(String salarys) {
		this.salarys = salarys;
	}
	public String getMyRemark() {
		return myRemark;
	}
	public void setMyRemark(String myRemark) {
		this.myRemark = myRemark;
	}
	
}
